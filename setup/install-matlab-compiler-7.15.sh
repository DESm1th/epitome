#!/bin/bash

echo '************************************************************************'
echo '*********************** Grabbing data from AFNI ************************'
echo '************************************************************************'
echo ''

mkdir tmp 
cd tmp
wget -c http://afni.nimh.nih.gov/sscc/staff/glend/matlab_compiler/McRetroTS_linux64pkg.zip
unzip McRetroTS_linux64pkg.zip

echo '************************************************************************'
echo '********************** Installing MATLAB Compiler **********************'
echo '************************************************************************'
echo ''

sudo ./MCRInstaller.bin

echo '************************************************************************'
echo '***************************** Cleaning Up ******************************'
echo '************************************************************************'
echo ''

cd ../
rm -r tmp/

